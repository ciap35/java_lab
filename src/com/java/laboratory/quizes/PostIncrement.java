package com.java.laboratory.quizes;

public class PostIncrement {
    public static void main(String[] args){
        //Post-Increment i++
        /*
        (i++): we use i++ in our statement if we want to use the current value, and then we want to increment the value of i by 1.
        */
        int i=2;
        System.out.println("Post-Increment");
        System.out.println(3*i++);
    }
}
